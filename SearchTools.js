(function() {
    'use strict';

    let searchKeyMap={'google':'q'};


    function run(){
        let params = parseUrlParams(location.search);
        let searchStr = params[searchKeyMap.google];
        document.searchStr = searchStr;
        console.info(searchStr);
        if(!searchStr){
            return;
        }
        if(!/-.+$/.test(searchStr)){
            //改变参数重新请求
            searchStr+='+-csdn';
            params[searchKeyMap.google] = searchStr;
            let search = encodeToUrlStr(params);
            location.search = search;
        }else{
            //隐藏
            document.addEventListener('DOMContentLoaded', function () {
                searchValue(searchValue().replace(/ -.+$/,''));
              /*  $('.gLFyf.gsfi').oninput=function(e){
                    searchValue(searchValue().replace(/ -.+$/,''));
                }
                */

            });

        }
    }



    function searchValue(value){
        if(value){
            $('.gLFyf.gsfi').value= value;
        }else{
            return $('.gLFyf.gsfi').value;
        }
    }

    function parseUrlParams(str) {
        let result = {};
        if (!str) {
            return result;
        }
        if (str.startsWith('?')) {
            str = str.substring(1, str.length);
        }

        let list = str.split('&');
        list.forEach(function (s) {
            let args = s.split('=');
            if (!args || args.length !== 2) {
                return;
            }
            result[decodeURIComponent(args[0])] = decodeURIComponent(args[1]);
        });
        return result;
    }

    function isJson(obj) {
        return typeof(obj) === "object" && Object.prototype.toString.call(obj).toLowerCase() === "[object object]" && !obj.length;
    }

    function encodeToUrlStr(jsonObject) {

        if (!isJson(jsonObject)) {
            throw jsonObject + ": is not a json";
        }
        let result = '';
        for (let key in jsonObject) {
            result += key + '=' + jsonObject[key]+'&';
        }
        return result.substring(0,result.length-1);
    }

    run();
    // Your code here...
})();